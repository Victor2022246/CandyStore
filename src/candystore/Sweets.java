package candystore;

/**
 *
 * @author victor pc
 */
public class Sweets {
    String flavor;
    String name;
    String brand;

    public Sweets(String flavor, String name, String brand) {
        this.flavor = flavor;
        this.name = name;
        this.brand = brand;
    }

    public String getFlavor() {
        return flavor;
    }

    public String getName() {
        return name;
    }

    public String getBrand() {
        return brand;
    }
    
    
    
    
    
    
}
